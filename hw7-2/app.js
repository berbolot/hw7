const freeFire1 = document.querySelector('.fen1')
const free1 = document.querySelector('.otvet1')
freeFire1.addEventListener('click', function(){
    free1.classList.toggle('otvet1');
    if(freeFire1.innerHTML === "Скрыть ответ"){
        freeFire1.innerHTML = "Показать ответ";
    }else{
        freeFire1.innerHTML = "Скрыть ответ"
    }
})

